<script>
  import { onMount } from 'svelte';

  let timelineElement;

  onMount(() => {
    const handleScroll = () => {
      if (!timelineElement) return;
      const rect = timelineElement.getBoundingClientRect();
      const windowHeight = window.innerHeight;
      
      const timelineTop = rect.top;
      const timelineHeight = rect.height;
      
      const startTrigger = windowHeight * 0.75;
      const endTrigger = windowHeight * 0.25;
      
      let progress = 0;
      if (timelineTop < startTrigger) {
        const scrolled = startTrigger - timelineTop;
        const totalScrollable = timelineHeight - (startTrigger - endTrigger);
        progress = Math.min(100, Math.max(0, (scrolled / totalScrollable) * 100));
      }
      
      timelineElement.style.setProperty("--timeline-progress", `${progress}%`);
    };

    window.addEventListener("scroll", handleScroll, { passive: true });
    handleScroll(); // Trigger initial check

    return () => {
      window.removeEventListener("scroll", handleScroll);
    };
  });
</script>

