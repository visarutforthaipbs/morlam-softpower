  let playingId = null;
  let audioElements = {};

  function toggleAudio(id) {
    const audio = audioElements[id];
    if (!audio) return;

    if (playingId === id) {
      // Pause current track
      audio.pause();
      playingId = null;
    } else {
      // Stop and reset all other tracks
      Object.keys(audioElements).forEach((key) => {
        if (key !== id && audioElements[key]) {
          audioElements[key].pause();
          audioElements[key].currentTime = 0;
        }
      });

      // Pause ambient loop via event
      dispatch('playtrack');

      // Play new track
      audio.play().then(() => {
        playingId = id;
      }).catch(err => {
        console.error("Audio playback error:", err);
      });
    }
  }

  function handleAudioEnded(id) {
    if (playingId === id) {
      playingId = null;
    }
  }
